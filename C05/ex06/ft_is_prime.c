int	ft_is_prime(int nb)
{
	int	prime;
	int	flag;

	flag = 1;
	prime = 2;
	if (nb >= 2)
	{
		while (prime < nb / 2)
		{
			if (nb % prime == 0)
				return (0);
			prime++;
		}
		if (flag == 1)
			return (1);
	}
	return (0);
}
